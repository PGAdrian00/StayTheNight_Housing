package com.example.staythenight_housing;

public class Hotel {
    private String hotelName;
    private String hotelAddress;
    private String hotelPhoneNr;
    private String hotelWebpage;

    public Hotel(String hotelName, String hotelAddress, String hotelPhoneNr, String hotelWebpage) {
        this.hotelName = hotelName;
        this.hotelAddress = hotelAddress;
        this.hotelPhoneNr = hotelPhoneNr;
        this.hotelWebpage = hotelWebpage;
    }
}
